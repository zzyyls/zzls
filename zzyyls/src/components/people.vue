            <h4>预防</h4>
            <h5>如何预防颈椎病？</h5>
            <p>要定期改变头颈部体位，每当伏案工作过久后，应抬头远视几分钟，待眼睛疲劳消除后再工作；加强颈肩部肌肉的锻炼，在工间或工余时，做头及双上肢的前屈、后伸及旋转运动；避免高枕睡眠的不良习惯，高枕使头部前屈，增大下位颈椎的应力，有加速颈椎退变的可能，枕头不宜过高或过低，切忌 「高枕无忧」，以生理位为佳，一般讲，枕头高以 8~15 cm 为宜，或按公式计算：（肩宽 - 头宽）÷ 2；注意颈肩部保暖，避免头颈负重物，避免过度疲劳，坐车时不要打瞌睡。</p>
