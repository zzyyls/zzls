            <h4>诊断</h4>
            <h5>有哪些症状提示可能得了颈椎病？</h5>
            <p>颈椎病的种类颇多，症状复杂，有时往往只有一个表现， 由于颈椎病容易误诊，如果身体有以下表现，就要想到是不是颈椎出了问题：走路有踩棉花感、经常颈肩部疼痛，颈部不适，上肢麻木放射痛，行走发飘乏力，久治不愈的头痛或偏头痛；久治不愈的头晕；非耳部原因的持续耳鸣或听力下降；不明原因的心律不齐、类似心绞痛的症状等。</p>
            <h5>怀疑颈椎病要做哪些检查？</h5>
            <p>颈椎病的检查包括物理检查和辅助检查，物理检查要经医生检查明确有无颈椎病的症状，包括局部是否有压痛点、颈椎活动范围及一些颈椎试验检查。此外，为了定位诊断或鉴别诊断，对感觉、运动、反射等神经系统方面的检查，有时也酌情予以选择。例如：手部和上肢的感觉障碍分布区，与受累颈椎椎节定位有直接关系。运动检查，主要是进行肌张力、肌力、步态等方面的检查。</p>
            <p>辅助检查有 X 线检查，磁共振成像，CT，还有脊髓造影、椎动脉造影等。另外，为了鉴别诊断，可根据需要进行脑脊液检查、肌电图、脑血流图等检查。</p>
            <h5>X 线检查对颈椎病诊断有什么意义？</h5>
            <p>常规的辅助检查，主要为 X 线检查，可拍摄颈椎正位、侧位、斜位平片等。正位：观察有无枢环关节脱位、齿状突骨折或缺失等先天性异常；第七颈椎横突有无过长，有无颈肋；钩椎关节及椎间隙有无增宽或变窄。</p>
