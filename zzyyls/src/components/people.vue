            <h4>症状</h4>
            <h5>神经根型</h5>
            <p>发病率最高，表现为受累神经根对应区域的神经痛、感觉障碍、麻木，异样感，活动不灵活。仰头、咳嗽、喷嚏可加重，就医时间越早，疗效越好，大部分病人可以部分缓解，不需手术治疗。</p>
            <h5>脊髓型</h5>
            <p> 约 10~15% 病人为此型。当发生椎间盘突出、骨质增生，或连接骨关节的韧带增厚或骨化时，可压迫脊髓及血管，甚至发生脊髓缺血或坏死。早期为单侧或双侧下肢麻木，以后发展为行走困难和大小便功能障碍甚至瘫痪。</p>
            <h5>交感神经型</h5>
            <p>约占 5%。因颈部交感神经受压产生的症状。因其分布广泛，因此可引起许多器官和系统的症状。包括头部症状：头晕，头痛，颈后痛；眼部症状：眼睑下垂，视物模糊，甚至失明；心脏症状：心跳加速或减慢，心口疼；周围症状：肢体、头、颈、面部发麻或疼痛；其他：耳鸣、耳聋等。</p>
            <h5>椎动脉型</h5>
            <p>约占 2%。是由于椎动脉受到外来的压迫或刺激，引起功能失调，脑部供血不足而产生的一系列症状。早期症状一般不明显，加重到一定程度，在特殊的体位下表现为突然发病，只要颈部向某个方向转动一下，当即出现眩晕，甚至感到天昏地暗。有时伴有头疼，恶心呕吐，耳鸣，视物不清等。</p>
