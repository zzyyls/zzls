            <h4>病因</h4>
            <h5>颈椎病的发病原因和机制是什么？</h5>
            <p>颈椎病是一种以退行性病理改变为基础的疾患，其发病原因很多，但综合起来有四个，包括颈椎的退行性变（颈椎增生，椎间盘突出等）、慢性劳损（最常见到的原因如低头工作等）、发育性颈椎椎管狭窄（多为先天性）、颈椎的先天性畸形（比较少见，如蝴蝶椎、并椎等）。</p>
            <p>前两者是主要原因，由于颈椎长期劳损、骨质增生，或椎间盘脱出、韧带增厚，致使颈椎脊髓、神经根或椎动脉受压，表现为颈椎间盘退变本身及其继发性的一系列病理改变，如椎节失稳、松动；髓核突出或脱出；骨刺形成；韧带肥厚和继发的椎管狭窄等，刺激或压迫了邻近的神经根、脊髓、椎动脉及颈部交感神经等组织，引起一系列症状和体征。</p>
            <h4>颈椎病是不是就是椎间盘突出引起的？</h4>
            <p>椎间盘突出是颈椎病的常见原因之一，是颈椎退变的一种表现形式（类似于老人的头发变白），可以导致脊髓及血管神经压迫，引起颈椎病，但不是唯一原因，有些患者椎间盘突出很明显，但常常没有临床症状，也无需特殊治疗。</p>
            <h4>颈椎病是不是就是颈椎骨质增生？</h4>
            <p>颈椎退行性改变是颈椎病发病的主要原因，颈椎的骨质增生（骨刺）也是中老年人的一种退变表现形式，是一种自然地老化，本身没有什么危害，甚至对于维持椎体稳定还有一定好处，所以不可谈起骨刺色变，但骨质增生如果压迫颈椎周围的神经血管就可导致颈椎病，那就需要治疗了。</p>
            <h4>颈椎病是不是老年人才会发生的？</h4>
            <p>颈椎病是中老年人的常见病、多发病之一。有统计表明，在 50 岁左右的人群中大约有 25% 的人患过或正患此病，60 岁左右则达 50%。调查显示，全国大约有 7%~10% 的人患颈椎病，但目前，普遍存在对该病早期重视不够，不能在出现症状时自我识别判断，致使该病发展到晚期可能致人瘫痪等危害。 但近年来罹患此病的年轻人有增加趋势，且工作集中在头颈部长期处于同一姿势的人，或头颈部活动频繁、从事颈部容易受伤职业者，像是久坐办公桌的白领阶层、电脑操作员、会计、教师、学生等，这些需长期伏案工作的职业，如果姿势不正确，罹患颈椎病的机率比一般人高了 4 到 6 倍，所以也不可掉以轻心。</p>
