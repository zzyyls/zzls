            <h4>生活</h4>
            <h5>颈椎病与从事的工作有关么？</h5>
            <p>颈椎病是中老年专利的传统正逐渐为现实所改写， 现代文明的进步，体力劳动逐渐被机器取代，伏案工作的增加及运动量的不足使颈椎病发生率迅速上升，并且明显趋向低龄化。大量统计材料表明某些工作量不大，强度不高，但处于坐位，尤其是低头工作者的颈椎病发病率特高，包括家务劳动者、刺绣女工、办公室人员、打字抄写者、仪表流水线上的装配工等。</p>
            <h5>哪些运动有助于缓解颈椎病症状？</h5>
            <p>运动是颈椎病的最好治疗方法之一，运动能够增加自身体质、锻炼肌肉、坚固骨骼，而且能使处于紧张状态下的肌肉得到松弛，从而达到防治颈椎病的效果。较好的运动如太极拳、羽毛球、乒乓球、游泳、放风筝等。太极拳的招式中非常重视站姿及头、眼、颈和四肢的配合和协调，在打羽毛球的过程中，头部会随着羽毛球的运动做前屈、后伸、 左转、右转等放松性运动，对于长期伏案、埋头工作的上班族而言，在回接高球时，抬头看球的动作还可以很好地起到「反向治疗」的效果。且向后引臂的回球动作，亦能令颈椎与脊椎处于放松状态，缓解肩膀酸痛，因而能很好地防治颈椎病。</p>
            <h5>颈椎病有哪些家庭物理疗法？</h5>
            <p>家庭物理疗法不仅可以巩固正规治疗的疗效，降低治愈后的复发，而且可提高病者对疾病的深入了解和增强保健意识，减少经济开支。最易进行的是温热敷和红外线等理疗。热毛巾、热水袋、热水澡等都是进行温热敷的便利条件。加热的石蜡、白炽灯等则是很好的红外线发射器。此外，热敷袋、场效应治疗仪、小型红外线辐射灯等也可应用于家庭理疗。这些简易的理疗方法可改善血液循环，缓解肌肉痉挛，消除肿胀和减轻症状，有助于巩固和提高正规治疗的效果。</p>
